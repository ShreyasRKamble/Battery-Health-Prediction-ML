def compute_vif(dataframe):
    vif_data = pd.DataFrame()
    vif_data["Feature"] = dataframe.columns
    vif_data["VIF"] = [
        variance_inflation_factor(dataframe.values, i)
        for i in range(dataframe.shape[1])
    ]
    return vif_data.sort_values("VIF", ascending=False).reset_index(drop=True)

VIF_THRESHOLD = 10.0
X_vif = X.copy()

X_vif = X_vif.dropna(axis=1)

print("\n─── Iterative VIF Feature Elimination ───")
iteration = 1
while True:
    vif_df = compute_vif(X_vif)
    print(f"\nIteration {iteration}:")
    print(vif_df.to_string(index=False))

    max_vif = vif_df["VIF"].max()
    if max_vif <= VIF_THRESHOLD:
        print(f"\n All VIF values ≤ {VIF_THRESHOLD}. Stopping.")
        break

    drop_feature = vif_df.loc[vif_df["VIF"].idxmax(), "Feature"]
    print(f"\n Dropping '{drop_feature}' (VIF = {max_vif:.2f})")
    X_vif = X_vif.drop(columns=[drop_feature])
    iteration += 1

retained_features = X_vif.columns.tolist()
